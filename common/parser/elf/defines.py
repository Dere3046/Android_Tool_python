# OS-specific flags mask and shift
P_FLAGS_OS_SEGMENT_TYPE_MASK = 0x7000000
P_FLAGS_OS_SEGMENT_TYPE_SHIFT = 24

def P_FLAGS_OS_SEGMENT_TYPE(p_flags):
    """Extract OS-specific segment type field."""
    return (p_flags & P_FLAGS_OS_SEGMENT_TYPE_MASK) >> P_FLAGS_OS_SEGMENT_TYPE_SHIFT

# Page mode mask and shift
P_FLAGS_OS_PAGE_MODE_MASK = 0x100000
P_FLAGS_OS_PAGE_MODE_SHIFT = 20

def P_FLAGS_OS_PAGE_MODE(p_flags):
    """Extract OS-specific page mode field."""
    return (p_flags & P_FLAGS_OS_PAGE_MODE_MASK) >> P_FLAGS_OS_PAGE_MODE_SHIFT

